# STATISTICaL OPERATIONS


import numpy as np
a=np.array([6,2,45,7,99,1])

# MAX()
print(np.max(a))

# MIN()
print(np.min(a))

# SUM()
print(np.sum(a))

# MEAN()/AVERAGE()
print(np.mean(a))

# MEDIAN()-sort and find the middle value
print(np.median(a))

# PROD()
print(np.prod(a))

# VAR() 
print(np.var(a))

# STANDARD DEVIATION/ STD() - square root of variance
print(np.std(a))

